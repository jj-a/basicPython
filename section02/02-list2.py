# 다차원(배열형) 리스트

stud1 = ["홍길동", 98]
stud2 = ["무궁화", 82]
stud3 = ["라일락", 72]

# 1차원 리스트
# 새로운 리스트 stud_group의 각 원소가 리스트가 되는 형태
stud_group = [stud1, stud2, stud3]


print(stud_group)

# 2차원 리스트
# 1차원 원소에 접근
print(stud_group[0])
print(stud_group[1])

# 2차원 원소에 접근
print(stud_group[0][0])
print(stud_group[0][1])


